function update(id) {
    var image = document.getElementById("image" + id);
    var nom = document.getElementById("nom" + id);
    var type = document.getElementById("type" + id);

    var nom_poke = nom.innerHTML.toLowerCase();

    var x = pokedex.indexOf(nom_poke) + 1

    var xhttp = new XMLHttpRequest();
    xhttp.onreadystatechange = function() {
        if (this.readyState == 4 && this.status == 200) {
            var poke = JSON.parse(this.responseText);
			image.src = poke.sprites.front_default;
			nom.innerHTML = nom_poke.toUpperCase();
			if (poke.types.length == 2){
				type.innerHTML = get_image(6,types[poke.types[0].type.name])+get_image(6,types[poke.types[1].type.name]);
			}
			else{
				type.innerHTML = get_image(12,types[poke.types[0].type.name]);
			}
        }
		else {
			image.src = "https://www.pokepedia.fr/images/5/54/Sprite_MissingNo._RV.png"
			type.innerHTML = ""
		}
    };
    xhttp.open("GET", `https://pokeapi.co/api/v2/pokemon-form/${x}/`, true);
    xhttp.send();
}
function get_image(col_largeur,type){
	return (`<div class="col-${col_largeur}"><img `+ ((col_largeur==12) ? 'class="poke"': "") +`src="https://www.pokebip.com/pokedex-images/types/${type}.png"></div>`)
}
